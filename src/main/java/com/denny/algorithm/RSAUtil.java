    public static Map<String, Object> init() throws Exception {
        BigInteger m = new BigInteger(mod, 16);
        BigInteger pubE = new BigInteger(publicE);
        BigInteger priE = new BigInteger(privateE, 16);

        RSAPublicKeySpec rsaPublicKeySpec = new RSAPublicKeySpec(m, pubE);
        KeyFactory keyFactory = KeyFactory.getInstance("RSA");
        RSAPublicKey publicKey = (RSAPublicKey) keyFactory.generatePublic(rsaPublicKeySpec);
        log.info("publicKey:{}", Base64.getEncoder().encodeToString(publicKey.getEncoded()));

        RSAPrivateKeySpec rsaPrivateKeySpec = new RSAPrivateKeySpec(m, priE);
        RSAPrivateKey privateKey = (RSAPrivateKey) keyFactory.generatePrivate(rsaPrivateKeySpec);
        log.info("privateKey:{}", Base64.getEncoder().encodeToString(privateKey.getEncoded()));
        Map<String, Object> keyMap = new HashMap<>();
        keyMap.put(PRIVATE_KEY, privateKey);
        keyMap.put(PUBLIC_KEY, publicKey);
        return keyMap;
    }

    public static byte[] encrypt(String content, PublicKey publicKey) throws Exception {
        byte[] data = content.getBytes("utf-8");
        KeyFactory keyFactory = KeyFactory.getInstance("RSA");
        Cipher cipher = Cipher.getInstance(keyFactory.getAlgorithm());
        cipher.init(Cipher.ENCRYPT_MODE, publicKey);
        int inputLen = data.length;
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        int offSet = 0;
        byte[] cache;
        int i = 0;
        // 对数据分段加密
        while (inputLen - offSet > 0) {
            if (inputLen - offSet > MAX_ENCRYPT_BLOCK) {
                cache = cipher.doFinal(data, offSet, MAX_ENCRYPT_BLOCK);
            } else {
                cache = cipher.doFinal(data, offSet, inputLen - offSet);
            }
            out.write(cache, 0, cache.length);
            i++;
            offSet = i * MAX_ENCRYPT_BLOCK;
        }
        byte[] encryptedData = out.toByteArray();
        out.close();
        return encryptedData;
    }
